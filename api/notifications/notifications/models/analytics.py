class OptOutTracking(models.Model):
    """
    Records an individual user opt-out (unsubscribe) from a specific channel.
    Used by OptOutService and unsubscribe_tasks to suppress future sends.
    """

    CHANNEL_CHOICES = NotificationInsight.CHANNEL_CHOICES

    REASON_CHOICES = (
        ('too_many', 'Too Many Notifications'),
        ('not_relevant', 'Not Relevant'),
        ('privacy', 'Privacy Concern'),
        ('spam', 'Marked as Spam'),
        ('user_request', 'User Request'),
        ('admin_action', 'Admin Action'),
        ('system', 'System / Automatic'),
        ('other', 'Other'),
    )

    user = models.ForeignKey(
        settings.AUTH_USER_MODEL,
        on_delete=models.CASCADE,
        related_name='opt_out_records',
    )

    channel = models.CharField(
        max_length=20,
        choices=CHANNEL_CHOICES,
        db_index=True,
    )

    # Whether the user is currently opted-out (False = re-subscribed)
    is_active = models.BooleanField(
        default=True,
        help_text='True = user is opted out; False = re-subscribed',
    )

    reason = models.CharField(
        max_length=30,
        choices=REASON_CHOICES,
        default='user_request',
    )

    notes = models.TextField(blank=True, help_text='Optional free-text reason')

    # The notification that triggered the opt-out (if applicable)
    triggered_by = models.ForeignKey(
        'notifications.Notification',
        on_delete=models.SET_NULL,
        null=True,
        blank=True,
        related_name='triggered_opt_outs',
    )

    # Who actioned this opt-out (user themselves vs admin)
    actioned_by = models.ForeignKey(
        settings.AUTH_USER_MODEL,
        on_delete=models.SET_NULL,
        null=True,
        blank=True,
        related_name='actioned_opt_outs',
    )

    opted_out_at = models.DateTimeField(default=timezone.now)
    opted_in_at = models.DateTimeField(
        null=True,
        blank=True,
        help_text='When the user re-subscribed (if applicable)',
    )

    # Timestamps
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        app_label = 'notifications'
        verbose_name = 'Opt-Out Record'
        verbose_name_plural = 'Opt-Out Records'
        ordering = ['-opted_out_at']
        indexes = [
            models.Index(fields=['user', 'channel', 'is_active']),
            models.Index(fields=['channel', 'is_active']),
            models.Index(fields=['opted_out_at']),
        ]

    def __str__(self):
        status = 'opted-out' if self.is_active else 'resubscribed'
        return f"{self.user} — {self.channel} ({status})"

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def resubscribe(self, save=True):
        """Mark the user as re-subscribed to this channel."""
        self.is_active = False
        self.opted_in_at = timezone.now()
        if save:
            self.save(update_fields=['is_active', 'opted_in_at', 'updated_at'])

    @classmethod
    def is_opted_out(cls, user, channel: str) -> bool:
        """Return True if the user is currently opted out of the given channel."""
        return cls.objects.filter(user=user, channel=channel, is_active=True).exists()

    @classmethod
    def opt_out(cls, user, channel: str, reason='user_request', notes='',
                triggered_by=None, actioned_by=None):
        """
        Create a new opt-out record for the user/channel combination.
        If one already exists and is active, updates it. Returns the instance.
        """
        obj, created = cls.objects.update_or_create(
            user=user,
            channel=channel,
            defaults={
                'is_active': True,
                'reason': reason,
                'notes': notes,
                'triggered_by': triggered_by,
                'actioned_by': actioned_by,
                'opted_out_at': timezone.now(),
                'opted_in_at': None,
            },
        )
        return obj


