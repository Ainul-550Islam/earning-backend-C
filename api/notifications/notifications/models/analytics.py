class NotificationFatigue(models.Model):
    """
    Per-user daily/weekly send counters used by FatigueService to prevent
    notification overload. Refreshed every day by fatigue_check_tasks.py.
    """

    user = models.OneToOneField(
        settings.AUTH_USER_MODEL,
        on_delete=models.CASCADE,
        related_name='notification_fatigue',
    )

    # Counters (reset daily / weekly by the fatigue check task)
    sent_today = models.PositiveSmallIntegerField(default=0)
    sent_this_week = models.PositiveSmallIntegerField(default=0)
    sent_this_month = models.PositiveIntegerField(default=0)

    # Thresholds (can be overridden per-user; 0 = use system default)
    daily_limit = models.PositiveSmallIntegerField(
        default=0,
        help_text='0 = use system default from settings',
    )
    weekly_limit = models.PositiveSmallIntegerField(
        default=0,
        help_text='0 = use system default from settings',
    )

    # Fatigue flag — set by FatigueService when limits are exceeded
    is_fatigued = models.BooleanField(
        default=False,
        db_index=True,
        help_text='True = suppress non-critical notifications for this user',
    )

    # When was the fatigue flag last evaluated
    last_evaluated_at = models.DateTimeField(null=True, blank=True)

    # Last reset timestamps
    daily_reset_at = models.DateTimeField(null=True, blank=True)
    weekly_reset_at = models.DateTimeField(null=True, blank=True)

    # Timestamps
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        app_label = 'notifications'
        verbose_name = 'Notification Fatigue'
        verbose_name_plural = 'Notification Fatigue Records'
        ordering = ['-updated_at']
        indexes = [
            models.Index(fields=['is_fatigued']),
            models.Index(fields=['user']),
        ]

    def __str__(self):
        fatigued_label = ' [FATIGUED]' if self.is_fatigued else ''
        return f"Fatigue({self.user}) today={self.sent_today} week={self.sent_this_week}{fatigued_label}"

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    DEFAULT_DAILY_LIMIT = 10
    DEFAULT_WEEKLY_LIMIT = 50

    def get_effective_daily_limit(self) -> int:
        """Return the user's daily limit (fall back to system default)."""
        return self.daily_limit or self.DEFAULT_DAILY_LIMIT

    def get_effective_weekly_limit(self) -> int:
        """Return the user's weekly limit (fall back to system default)."""
        return self.weekly_limit or self.DEFAULT_WEEKLY_LIMIT

    def increment(self, save=True):
        """Increment both daily and weekly counters by 1."""
        self.sent_today += 1
        self.sent_this_week += 1
        self.sent_this_month += 1
        if save:
            self.save(update_fields=['sent_today', 'sent_this_week', 'sent_this_month', 'updated_at'])

    def evaluate_fatigue(self, save=True) -> bool:
        """
        Determine whether the user is fatigued based on current counters.
        Updates the is_fatigued flag and last_evaluated_at.
        Returns True if fatigued.
        """
        self.is_fatigued = (
            self.sent_today >= self.get_effective_daily_limit()
            or self.sent_this_week >= self.get_effective_weekly_limit()
        )
        self.last_evaluated_at = timezone.now()
        if save:
            self.save(update_fields=['is_fatigued', 'last_evaluated_at', 'updated_at'])
        return self.is_fatigued

    def reset_daily(self, save=True):
        """Reset the daily counter (called each midnight by the task)."""
        self.sent_today = 0
        self.daily_reset_at = timezone.now()
        if save:
            self.save(update_fields=['sent_today', 'daily_reset_at', 'updated_at'])

    def reset_weekly(self, save=True):
        """Reset the weekly counter (called every Monday by the task)."""
        self.sent_this_week = 0
        self.weekly_reset_at = timezone.now()
        if save:
            self.save(update_fields=['sent_this_week', 'weekly_reset_at', 'updated_at'])

    def reset_monthly(self, save=True):
        """Reset the monthly counter."""
        self.sent_this_month = 0
        if save:
            self.save(update_fields=['sent_this_month', 'updated_at'])

    @classmethod
    def get_or_create_for_user(cls, user):
        """Get or create the fatigue record for a user."""
        obj, _ = cls.objects.get_or_create(user=user)
        return obj
