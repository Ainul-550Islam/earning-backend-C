# earning_backend/api/notifications/models/analytics.py
"""
Analytics & opt-out models:
  - NotificationInsight  — daily per-channel send/delivery/open/click/unsub metrics
  - DeliveryRate         — daily per-channel delivery, open, and click rate percentages
  - OptOutTracking       — individual user opt-out event per channel
  - NotificationFatigue  — per-user daily/weekly send counters + fatigue flag
"""

from django.db import models
from django.conf import settings
from django.utils import timezone


# ---------------------------------------------------------------------------
# NotificationInsight
# ---------------------------------------------------------------------------

class NotificationInsight(models.Model):
    """
    Stores daily aggregated send-metrics broken down by channel.
    Generated by the insight_tasks.py daily analytics task.
    """

    CHANNEL_CHOICES = (
        ('in_app', 'In-App'),
        ('push', 'Push'),
        ('email', 'Email'),
        ('sms', 'SMS'),
        ('telegram', 'Telegram'),
        ('whatsapp', 'WhatsApp'),
        ('browser', 'Browser Push'),
        ('all', 'All Channels'),
    )

    date = models.DateField(db_index=True)
    channel = models.CharField(
        max_length=20,
        choices=CHANNEL_CHOICES,
        db_index=True,
    )

    # Volume metrics
    sent = models.PositiveIntegerField(default=0)
    delivered = models.PositiveIntegerField(default=0)
    failed = models.PositiveIntegerField(default=0)
    opened = models.PositiveIntegerField(default=0)
    clicked = models.PositiveIntegerField(default=0)
    unsubscribed = models.PositiveIntegerField(default=0)

    # Unique users reached
    unique_users_reached = models.PositiveIntegerField(default=0)

    # Cost (sum of Notification.cost for the day/channel)
    total_cost = models.DecimalField(max_digits=12, decimal_places=4, default=0)
    cost_currency = models.CharField(max_length=10, default='USD')

    # Extra breakdown JSON (e.g. by notification type, by country)
    breakdown = models.JSONField(default=dict, blank=True)

    # Timestamps
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        app_label = 'notifications'
        verbose_name = 'Notification Insight'
        verbose_name_plural = 'Notification Insights'
        unique_together = [['date', 'channel']]
        ordering = ['-date', 'channel']
        indexes = [
            models.Index(fields=['date', 'channel']),
        ]

    def __str__(self):
        return f"Insight {self.date} / {self.channel} — sent: {self.sent}"

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    @property
    def delivery_rate(self):
        if self.sent == 0:
            return 0.0
        return round(self.delivered / self.sent * 100, 2)

    @property
    def open_rate(self):
        if self.delivered == 0:
            return 0.0
        return round(self.opened / self.delivered * 100, 2)

    @property
    def click_rate(self):
        if self.delivered == 0:
            return 0.0
        return round(self.clicked / self.delivered * 100, 2)

    def to_dict(self):
        return {
            'date': str(self.date),
            'channel': self.channel,
            'sent': self.sent,
            'delivered': self.delivered,
            'failed': self.failed,
            'opened': self.opened,
            'clicked': self.clicked,
            'unsubscribed': self.unsubscribed,
            'unique_users_reached': self.unique_users_reached,
            'delivery_rate': self.delivery_rate,
            'open_rate': self.open_rate,
            'click_rate': self.click_rate,
            'total_cost': float(self.total_cost),
            'cost_currency': self.cost_currency,
        }


# ---------------------------------------------------------------------------
# DeliveryRate
# ---------------------------------------------------------------------------

class DeliveryRate(models.Model):
    """
    Pre-computed daily delivery / open / click rate percentages per channel.
    Kept separate from NotificationInsight to allow lightweight queries from
    dashboards that only need the percentage metrics.
    """

    CHANNEL_CHOICES = NotificationInsight.CHANNEL_CHOICES  # reuse same choices

    date = models.DateField(db_index=True)
    channel = models.CharField(
        max_length=20,
        choices=CHANNEL_CHOICES,
        db_index=True,
    )

    delivery_pct = models.FloatField(
        default=0.0,
        help_text='Percentage of sent notifications that were delivered',
    )
    open_pct = models.FloatField(
        default=0.0,
        help_text='Percentage of delivered notifications that were opened',
    )
    click_pct = models.FloatField(
        default=0.0,
        help_text='Percentage of delivered notifications that were clicked',
    )

    # Number of data points used to compute the rates
    sample_size = models.PositiveIntegerField(default=0)

    # Timestamps
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        app_label = 'notifications'
        verbose_name = 'Delivery Rate'
        verbose_name_plural = 'Delivery Rates'
        unique_together = [['date', 'channel']]
        ordering = ['-date', 'channel']
        indexes = [
            models.Index(fields=['date', 'channel']),
        ]

    def __str__(self):
        return (
            f"DeliveryRate {self.date} / {self.channel} — "
            f"del:{self.delivery_pct}% open:{self.open_pct}% click:{self.click_pct}%"
        )

    @classmethod
    def upsert_from_insight(cls, insight: NotificationInsight):
        """
        Create or update a DeliveryRate record from a NotificationInsight row.
        """
        obj, _ = cls.objects.update_or_create(
            date=insight.date,
            channel=insight.channel,
            defaults={
                'delivery_pct': insight.delivery_rate,
                'open_pct': insight.open_rate,
                'click_pct': insight.click_rate,
                'sample_size': insight.sent,
            },
        )
        return obj


